=head1 NAME

DataDriven::Framework::Generator::Docs - Generate Markdown documentation from a Model

=head1 SYNOPSIS

    use DataDriven::Framework::Generator::Docs;

    my $gen = DataDriven::Framework::Generator::Docs->new(
        model      => $model,
        output_dir => 'docs/schema',
    );
    my @files = $gen->generate;

=head1 DESCRIPTION

Generates Markdown documentation files from a L<DataDriven::Framework::Model>.
Produces an index page (C<index.md>) and one page per concrete class.

=head2 Regeneration Safety

Generated sections are wrapped in HTML comment markers:

    <!-- BEGIN GENERATED -->
    ... generated content ...
    <!-- END GENERATED -->

On regeneration, only the content within the markers is replaced. Content
outside the markers is preserved, allowing you to add custom sections.

=head1 METHODS

=over 4

=item C<generate()>

Generates documentation into C<output_dir>. Returns a list of file paths written.

=back

=cut
